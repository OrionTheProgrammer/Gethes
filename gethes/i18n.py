def detect_system_language() -> str:
    candidates = []
    try:
        lang = locale.getlocale()[0]
        if lang:
            candidates.append(lang)
    except Exception:
        pass

    default_lang = None
    try:
        default_lang = locale.getdefaultlocale()[0]
    except Exception:
        pass
    if default_lang:
        candidates.append(default_lang)

    for item in candidates:
        code = item.split("_", 1)[0].lower()
        if code in SUPPORTED_LANGUAGES:
            return code
    return "en"


@dataclass
class I18n:
    mode: str
    active_language: str

    @classmethod
    def from_mode(cls, mode: str) -> "I18n":
        normalized_mode = mode if mode in LANG_MODE_VALUES else "auto"
        active = detect_system_language() if normalized_mode == "auto" else normalized_mode
        return cls(mode=normalized_mode, active_language=active)

    def set_mode(self, mode: str) -> None:
        normalized_mode = mode if mode in LANG_MODE_VALUES else "auto"
        self.mode = normalized_mode
        self.active_language = (
            detect_system_language() if normalized_mode == "auto" else normalized_mode
        )

    def t(self, key: str, **kwargs: object) -> str:
        lang_pack = TRANSLATIONS.get(self.active_language, TRANSLATIONS["en"])
        value = lang_pack.get(key) or TRANSLATIONS["en"].get(key) or key
        if kwargs:
            try:
                return value.format(**kwargs)
            except Exception:
                return value
        return value

